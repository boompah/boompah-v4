<strong><p>Process Outline</p></strong>

<p>I've always found when working with a team that having a consistent workflow can be efficient to ship your ideas. I've outlined my current workflow for responsive layout design with HTML wireframes created early on in the process.</p>

<ol>
	<li>Gather all the content from the client for each page if possible<em>,</em> and decide what will be designed initially <em>(usually the home page and an inner page)</em>. Specifically; navigation, text, images, social tools, sub menus, functionality, CTA's, forms, footer, etc. Obtain a vectorized version of the brands logo, high resolution images and any other brand content as needed.</li>
	<li>Review objectives, goals, project demands, inspiration links, etc. internally and with the client.</li>
	<li>Select which responsive framework, development kit or techniques you'd like to use for media queries and making the site responsive.</li>
	<li>Create and deliver <strong>HTML Wireframe(s)</strong> and <strong>2-3 look and feel concepts (JPGs)</strong> based off of #2. I also like to use this step to propose technologies as needed; jQuery slideshows, menu's, content sliders, etc. This way I can ensure my design(s) will respond accordingly early on and it gives the client a better understanding of what to expect on tablet and mobile resolutions.</li>
	<li>Review and refine the look and feel concepts with other designers. This step can allow the layout(s) to evolve to a next level of design by having a new perspective on the project. <em>This step would be skipped of course if I'm working solo.</em></li>
	<li>Review and finalize look and feel with the client.</li>
	<li>Update the html wireframe(s) to match the finalized mockups along with integrating technologies as needed; jQuery, API's, Wordpress, etc.</li>
	<li>Hand off HTML &amp; CSS to developers to implement into a CMS or whatever else.</li>
	<li>Deploy to a staging server for review and bug fixes/cross-browser testing.</li>
	<li>Deploy to the production server and celebrate an awesome project!</li>
</ol>

